# Code to find multiplication and division of two numbers to have an output

# Get input from user/terminal
number1 = int(input("Enter first number (number1): "))
number2 = int(input("Enter second number (number2): "))

# Calculate multiplication of the two numbers from the output
multiplication = number1 * number2

# Calculate division of the two numbers from the output
division = number1 / number2

# Output the results
print(f"\nResults:")
print(f"Multiplication: {number1} * {number2} = {multiplication}")
print(f"Division: {number1} / {number2} = {division}")
