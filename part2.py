# Program to find multiplication and division of two numbers

# Get input from user
number1 = int(input("Enter first number (num1): "))
number2 = int(input("Enter second number (num2): "))

# Calculate multiplication
multiplication = number1 * number2

# Calculate division
division = number1 / number2

# Display results
print(f"\nResults:")
print(f"Multiplication: {number1} * {number2} = {multiplication}")
print(f"Division: {number1} / {number2} = {division}")
