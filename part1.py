# Code to add and subtract two numbers, in order to have an output

# Get input from user/terminal
number1 = int(input("Enter first number (number1): "))
number2 = int(input("Enter second number (number2): "))

# Calculate addition of the two numbers
addition = number1 + number2

# Calculate subtraction of the two numbers
subtraction = number1 - number2

# Output the results
print(f"\nResults:")
print(f"Addition: {number1} + {number2} = {addition}")
print(f"Subtraction: {number1} - {number2} = {subtraction}")